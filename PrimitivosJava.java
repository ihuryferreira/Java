package primitivosjava;

public class PrimitivosJava {

    public static void main(String[] args) {
        //Os 8 primitivos do java
        
        //Primeiro byte: um inteiro de 8 bits (signed)
        byte a = -128; byte b = 127;
        //segundo short: um inteiro de 32 bits (signed)
        short c = -32768; short d = 3277;
        //Terceiro int: um inteiro de 32 bits (signed)
        int e = -2147483648; int f = 2147483647;
        //Quarto long: um inteiro de 64 bits (signed)
        long g = -9223372036854775808L; long h = 9223372036854775807L;
        //Quinto float: um número de ponto flutuante de 32 bits (signed)
        float i = -100.4345f; float j = 123243.4345f;
        //Sexto double: um número de ponto flutuante de 64 bits (signed)
        double k = -3123.434354; double l = 321321.3123435;
        //Setimo boolean:um valor indicando verdadeiro ou falso
        boolean m = false; boolean n = true;
        //Oitavo char: um caracter unicode (16-bit unsigned)
        char o = 'a'; char p = '4'; char q = '?';
        //Fim da Variavel
        
        //imprimindo a Variaveis Primitivos
        System.out.println("OS 8 PRIMITIVOS DO JAVA !!\n");
        System.out.println("=====================================\n");
        System.out.println("Primeira primitivo do java: Byte "+ a);
        System.out.println("Primeira primitivo do java: Byte "+ b);
        System.out.println("=====================================\n");
        System.out.println("segundo primitivo do java: short "+ c);
        System.out.println("segundo primitivo do java: short "+ d);
        System.out.println("=====================================\n");
        System.out.println("terceira primitivo do java: Int "+ e);
        System.out.println("terceira primitivo do java: Int "+ f);
        System.out.println("=====================================\n");
        System.out.println("quarta primitivo do java: long "+ g);
        System.out.println("quarta primitivo do java: long "+ h);
        System.out.println("=====================================\n");
        System.out.println("quinta primitivo do java: float "+ i);
        System.out.println("quinta primitivo do java: float "+ j);
        System.out.println("=====================================\n");
        System.out.println("sexta primitivo do java: double "+ k);
        System.out.println("sexta primitivo do java: double "+ l);
        System.out.println("=====================================\n");
        System.out.println("setima primitivo do java: boolean "+ m);
        System.out.println("setima primitivo do java: boolean "+ n);
        System.out.println("=====================================\n");
        System.out.println("Oitava primitivo do java: char "+ o);
        System.out.println("Oitava primitivo do java: char "+ p);
        System.out.println("Oitava primitivo do java: char "+ q);
    }
    
}
